/*
Задача 1. Наибольшее из пяти чисел
Ввести пять чисел и вывести наибольшее из них
Нужно напечатать наибольшее число
Данные на входе: Пять целых чисел разделенных пробелом
Данные на выходе: Одно целое число
Данные на входе: 4 15 9 56 4
Данные на выходе: 56
-----
Задача 2. Наименьшее из пяти чисел
Ввести пять чисел и вывести наименьшее из них
Нужно напечатать наименьшее число
Данные на входе: Пять целых чисел разделенных пробелом
Данные на выходе: Одно целое число
Данные на входе: 4 15 9 56 4
Данные на выходе: 4
-----
Задача 3. В порядке возрастания
Ввести три числа и определить, верно ли, что они вводились в порядке
возрастания.
Данные на входе: Три целых числа
Данные на выходе: Одно слово YES или NO
Пример
Данные на входе: 4 5 17
Данные на выходе: YES
-----
Задача 4. Какое время года
Ввести номер месяца и вывести название времени года.
Данные на входе: Целое число от 1 до 12 - номер месяца.
Данные на выходе: Время года на английском: winter, spring, summer, autumn
Пример №1
Данные на входе: 4
Данные на выходе: spring
Пример №2
Данные на входе: 1
Данные на выходе: winter

*/

#include <stdio.h>
#include <locale.h>
#include <string.h>
#include <ctype.h>
#include <limits.h>

// 1. Ввести пять чисел и вывести наибольшее из них
void task1()
{
    int a, b, c, d, e;
    int max;

    printf("Введите пять целых чисел, разделенных пробелом: ");
    scanf("%d %d %d %d %d", &a, &b, &c, &d, &e);

    // Находим максимальное число
    max = a;
    if (b > max)
    {
        max = b;
    }
    if (c > max)
    {
        max = c;
    }
    if (d > max)
    {
        max = d;
    }
    if (e > max)
    {
        max = e;
    }

    printf("Наибольшее число: %d\n", max);
}

// 2. Ввести пять чисел и вывести наименьшее из них
void task2()
{
    int a, b, c, d, e;
    int min;

    printf("Введите пять целых чисел, разделенных пробелом: ");
    scanf("%d %d %d %d %d", &a, &b, &c, &d, &e);

    // Находим минимальное число
    min = INT_MAX;
    if (a < min)
    {
        min = b;
    }
    if (b < min)
    {
        min = b;
    }
    if (c < min)
    {
        min = c;
    }
    if (d < min)
    {
        min = d;
    }
    if (e < min)
    {
        min = e;
    }

    printf("Наименьшее число: %d\n", min);
}

// 3. Ввести три числа и определить, верно ли, что они вводились в порядке возрастания.
void task3()
{
    int a, b, c;

    printf("Введите три числа: ");
    scanf("%d %d %d", &a, &b, &c);

    if (a < b && b < c)
    {
        printf("YES\n");
    }
    else
    {
        printf("NO\n");
    }
}

// 4. Ввести номер месяца и вывести название времени года.
void task4()
{
    int month;

    printf("Введите номер месяца: ");
    scanf("%d", &month);

    if (month == 12 || month == 1 || month == 2)
    {
        printf("winter\n");
    }
    else if (month >= 3 && month <= 5)
    {
        printf("spring\n");
    }
    else if (month >= 6 && month <= 8)
    {
        printf("summer\n");
    }
    else if (month >= 9 && month <= 11)
    {
        printf("autumn\n");
    }
    else
    {
        printf("Некорректный номер месяца\n");
    }
}

int main()
{
    setlocale(LC_ALL, "Rus");

    int choice;
    int exit = 0;

    while (!exit)
    {
        printf("\n-----------------------------\n");
        printf("1. Ввести пять чисел и вывести наибольшее из них\n");
        printf("2. Ввести пять чисел и вывести наименьшее из них\n");
        printf("3. Ввести три числа и определить, верно ли, что они вводились в порядке возрастания.\n");
        printf("4. Ввести номер месяца и вывести название времени года.\n");

        printf("<<< Выберите пункт меню или введите иное для выхода: ");

        if (scanf("%d", &choice) != 1)
        {
            choice = -1;
        }
        printf(">>>\n");

        switch (choice)
        {
        case 1:
            task1();
            break;

        case 2:
            task2();
            break;

        case 3:
            task3();
            break;

        case 4:
            task4();
            break;

        default:
            exit = 1;
            printf("END\n");
        }
    }

    return 0;
}
